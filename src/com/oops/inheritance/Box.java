package com.oops.inheritance;

public class Box {
    int length;
    int breadth;
    int height;

    Box(){

    }

    Box(int l, int b){

        this.length = l;
        this.breadth = b;

    }

    Box(int l, int b, int h){

        this.length = l;
        this.breadth = b;
        this.height = h;

    }
}
